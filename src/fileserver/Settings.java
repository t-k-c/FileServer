    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
         // TODO add your handling code here:
        SettingsManager.echo("click");
        if(SettingsManager.readSetting(SettingsManager.AUTH_ACCESS).equals(SettingsManager.AUTH_ACCESS_TRUE)){
            if(SettingsManager.setAuthAccess(false,null))
            { jLabel6.setIcon(new ImageIcon(getClass().getResource(toggleButtonOff)));
             jTextField2.setEditable(true);
            }
            else notify("An unknown error occurred");
        }
        else if(SettingsManager.readSetting(SettingsManager.AUTH_ACCESS).equals(SettingsManager.AUTH_ACCESS_FALSE))
        {
            String value;
            if(!(value = jTextField2.getText()).equals("")){
                if(SettingsManager.setAuthAccess(true,value)){ jLabel6.setIcon(new ImageIcon(getClass().getResource(toggleButtonOn)));
                 jTextField2.setEditable(false);}
                else notify("An unknown error occurred");
            }
            else{
                notify("The corresponding field was left empty");
            }
        }
    }//GEN-LAST:event_jLabel6MouseClicked
