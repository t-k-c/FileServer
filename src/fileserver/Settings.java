    private void jLabel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel15MouseClicked
        // TODO add your handling code here:
               // TODO add your handling code here:
         // TODO add your handling code here:
        SettingsManager.echo("click");
        if(SettingsManager.readSetting(SettingsManager.CUSTOM_PORT_STATUS).equals(SettingsManager.CUSTOM_PORT_STATUS_ON)){
            if(SettingsManager.setCustomPort(false,null)){ jLabel15.setIcon(new ImageIcon(getClass().getResource(toggleButtonOff)));
            jTextField3.setEditable(true);
            }
            else notify("An unknown error occurred");
        }
        else if(SettingsManager.readSetting(SettingsManager.CUSTOM_PORT_STATUS).equals(SettingsManager.CUSTOM_PORT_STATUS_OFF))
        {
            String value;
            if(!(value = jTextField3.getText()).equals("")){
               try{ if(Integer.parseInt(value) <= 60000){
                if(SettingsManager.setCustomPort(true,value)) {jLabel15.setIcon(new ImageIcon(getClass().getResource(toggleButtonOn)));
                jTextField3.setEditable(false);
                }
                else notify("An unknown error occurred");
                }
                else{
                    notify("wrong input value (does not fall in range) : "+value);
                }
            }catch(NumberFormatException e){
                 notify("wrong input value: "+value);
            } 
            }
            else{
                notify("The corresponding field was left empty");
            }
        }
    }//GEN-LAST:event_jLabel15MouseClicked

